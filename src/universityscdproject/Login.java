    private void LoginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginBtnActionPerformed
  
 
String userEmail = email1.getText().trim();
    String userPass = new String(password.getPassword());

    // 1. Validation
    if(userEmail.isEmpty() || userPass.isEmpty()){
        JOptionPane.showMessageDialog(this, "Email and Password are required");
        return;
    }
    
    if(!userEmail.contains("@") || !userEmail.contains(".")) {
        JOptionPane.showMessageDialog(this, "Please enter a valid email address");
        return;
    }

     try {
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/car_rental_db", "root", "");
        String query = "SELECT * FROM users WHERE email=? AND password=?";
        PreparedStatement pst = con.prepareStatement(query);
        pst.setString(1, userEmail);
        pst.setString(2, userPass);
        ResultSet rs = pst.executeQuery();

        if (rs.next()) {
             if (userEmail.equalsIgnoreCase("admin@gmail.com")) { 
                 new admin().setVisible(true); 
            } else {
                 Home homeFrame = new Home();
                homeFrame.setLoggedInEmail(rs.getString("email")); 
                homeFrame.setUser(rs.getString("full_name"));
                homeFrame.setVisible(true);
                homeFrame.pack();
                homeFrame.setLocationRelativeTo(null);
            }
            this.dispose(); 
        } else {
            JOptionPane.showMessageDialog(this, "Invalid Email or Password");
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Connection Error: " + e.getMessage());
    }


    }//GEN-LAST:event_LoginBtnActionPerformed

