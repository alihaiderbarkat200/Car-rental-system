    private void SignUpBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SignUpBtnActionPerformed
      String fullName = fname.getText().trim(); 
    String email = emailaddress.getText().trim();
    String pass = new String(password1.getPassword());

     if(fullName.isEmpty() || email.isEmpty() || pass.isEmpty()) {
        JOptionPane.showMessageDialog(this, "All fields are required!");
        return;
    }
    if(!isValidName(fullName)) {
        JOptionPane.showMessageDialog(this, "Please enter a valid name (letters only)");
        return;
    }
    if(!isValidEmail(email)) {
        JOptionPane.showMessageDialog(this, "Please enter a valid email address");
        return;
    }

     if (pass.length() < 8 || !pass.matches(".*[!@#$%^&*()_+\\-=\\[\\]{};':\"\\\\|,.<>\\/?].*")) {
        JOptionPane.showMessageDialog(this, "Security Error: Password must be at least 8 characters and include one special character (like @, #, $).");
        return;
    }

     try {
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/car_rental_db", "root", "");
        String checkQuery = "SELECT email FROM users WHERE email = ?";
        PreparedStatement checkPst = con.prepareStatement(checkQuery);
        checkPst.setString(1, email);
        ResultSet rs = checkPst.executeQuery();
        
        if(rs.next()){
            JOptionPane.showMessageDialog(this, "You already have an account with this email. Please go to Login.");
            clearFields(); 
            return;
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Database Connection Error: " + e.getMessage());
        return;
    }

     boolean verified = false;
    while (!verified) {
        generatedOTP = String.format("%06d", new java.util.Random().nextInt(999999));
        sendEmail(email, generatedOTP); 

        Object[] options = {"Verify", "Resend Code", "Cancel"};
        javax.swing.JTextField otpInput = new javax.swing.JTextField();
        Object[] message = {
            "A verification code was sent to: " + email,
            "Enter OTP:", otpInput
        };

        int choice = JOptionPane.showOptionDialog(this, message, "Security Verification",
                JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.INFORMATION_MESSAGE, null, options, options[0]);

        if (choice == 0) { 
            if (generatedOTP.equals(otpInput.getText().trim())) {
                verified = true;
                saveToDatabase(fullName, email, pass);
            } else {
                JOptionPane.showMessageDialog(this, "Incorrect OTP code. Try again.");
            }
        } 
        else if (choice == 1) { 
            continue; 
        } 
        else { 
            clearFields();  
            break; 
        }
    }


                
       
    }//GEN-LAST:event_SignUpBtnActionPerformed
private void saveToDatabase(String name, String email, String pass) {
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/car rental db", "root", ""); 
            String query = "INSERT INTO users (full_name, email, password, otp_code, is_verified) VALUES (?, ?, ?, ?, 1)";
            PreparedStatement pst = con.prepareStatement(query);
            pst.setString(1, name);
            pst.setString(2, email);
            pst.setString(3, pass);
            pst.setString(4, generatedOTP);
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Successfully Signed Up!");
            new Login().setVisible(true);
            this.dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "DB Error: " + e.getMessage());
        }
    }
