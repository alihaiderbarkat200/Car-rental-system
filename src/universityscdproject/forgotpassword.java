        String email = jTextField1.getText().trim();
        if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your email.");
            return;
        }

        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/car_rental_db", "root", "");
            String query = "SELECT * FROM users WHERE email = ?";
            PreparedStatement pst = con.prepareStatement(query);
            pst.setString(1, email);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                generatedOTP = String.format("%06d", new Random().nextInt(999999));
                sendEmail(email, generatedOTP); 
                JOptionPane.showMessageDialog(this, "OTP has been sent to your email.");
            } else {
                JOptionPane.showMessageDialog(this, "No account found with this email.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }

