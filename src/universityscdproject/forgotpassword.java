    private void sendEmail(String userEmail, String otp) {
        final String fromEmail = "alihaiderbarkat21@gmail.com"; 
        final String password = "pxptfxwgvttgvjfg"; 

        Properties props = new Properties();
        props.put("mail.smtp.auth", "true");
        props.put("mail.smtp.starttls.enable", "true");
        props.put("mail.smtp.host", "smtp.gmail.com");
        props.put("mail.smtp.port", "587");

        Session session = Session.getInstance(props, new javax.mail.Authenticator() {
            protected PasswordAuthentication getPasswordAuthentication() {
                return new PasswordAuthentication(fromEmail, password);
            }
        });

        try {
            Message message = new MimeMessage(session);
            message.setFrom(new InternetAddress(fromEmail));
            message.setRecipients(Message.RecipientType.TO, InternetAddress.parse(userEmail));
            message.setSubject("Password Reset OTP");
            message.setText("Your OTP for resetting your password is: " + otp);
            Transport.send(message);
        } catch (MessagingException e) {
            JOptionPane.showMessageDialog(this, "Email Error: " + e.getMessage());
        }
    }
    
   
