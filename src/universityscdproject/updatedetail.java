    private void submitbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitbtnActionPerformed
 
          String paymentMethod = jRadioButton2.isSelected() ? "Card" : "Cash";
        String orderId = orderid1.getText().trim();
        String email = email1.getText().trim();
        String license = Drivingno.getText().trim();
        java.util.Date pickup = jDateChooser1.getDate();
        java.util.Date returnD = jDateChooser2.getDate();

         if (orderId.isEmpty() || pickup == null || returnD == null) {
            JOptionPane.showMessageDialog(this, "Please fill in all booking details (ID and Dates).");
            return;
        }

         String expiryToSave = ""; 
        if (paymentMethod.equals("Card")) {
            expiryToSave = exp1.getText().trim();
            if (!expiryToSave.matches("\\d{2}/\\d{2}")) {
                JOptionPane.showMessageDialog(this, "Format Error: Use MM/YY for Expiry.");
                return;
            }
        }

        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/car_rental_db", "root", "");
            
             String updateBookingSQL = "UPDATE bookings SET user_email=?, drivinglicence=?, pickup_date=?, return_date=?, payment_method=?, status='Pending' WHERE `order-ID`=?";
            PreparedStatement pstBook = con.prepareStatement(updateBookingSQL);
            pstBook.setString(1, email);
            pstBook.setString(2, license);
            pstBook.setDate(3, new java.sql.Date(pickup.getTime()));
            pstBook.setDate(4, new java.sql.Date(returnD.getTime()));
            pstBook.setString(5, paymentMethod);
            pstBook.setString(6, orderId);
            pstBook.executeUpdate();

             if (paymentMethod.equals("Card")) {
                String checkSQL = "SELECT * FROM payments WHERE booking_id = (SELECT id FROM bookings WHERE `order-ID` = ?)";
                PreparedStatement pstCheck = con.prepareStatement(checkSQL);
                pstCheck.setString(1, orderId);
                ResultSet rs = pstCheck.executeQuery();

                if (rs.next()) {
                    String updatePaySQL = "UPDATE payments SET card_number=?, cvv=?, expiry_month=?, payment_method='Card' WHERE booking_id = (SELECT id FROM bookings WHERE `order-ID` = ?)";
                    PreparedStatement pstPay = con.prepareStatement(updatePaySQL);
                    pstPay.setString(1, card1.getText()); 
                    pstPay.setString(2, cvv1.getText());  
                    pstPay.setString(3, expiryToSave);  
                    pstPay.setString(4, orderId);
                    pstPay.executeUpdate();
                } else {
                    String insertPaySQL = "INSERT INTO payments (booking_id, user_email, amount, payment_method, card_number, cvv, expiry_month, transaction_time) " +
                                         "VALUES ((SELECT id FROM bookings WHERE `order-ID` = ?), ?, (SELECT total_price FROM bookings WHERE `order-ID` = ?), 'Card', ?, ?, ?, NOW())";
                    PreparedStatement pstInsert = con.prepareStatement(insertPaySQL);
                    pstInsert.setString(1, orderId);
                    pstInsert.setString(2, email);
                    pstInsert.setString(3, orderId);
                    pstInsert.setString(4, card1.getText());
                    pstInsert.setString(5, cvv1.getText());
                    pstInsert.setString(6, expiryToSave);
                    pstInsert.executeUpdate();
                }
            } else {
                String updateCashSQL = "UPDATE payments SET payment_method='Cash', card_number=NULL, cvv=NULL, expiry_month=NULL WHERE booking_id = (SELECT id FROM bookings WHERE `order-ID` = ?)";
                PreparedStatement pstCash = con.prepareStatement(updateCashSQL);
                pstCash.setString(1, orderId);
                pstCash.executeUpdate();
            }
            
            JOptionPane.showMessageDialog(this, "Update successful! Your request is pending Admin approval.");
            clearPage();
            con.close();
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage());
        }
    

    }//GEN-LAST:event_submitbtnActionPerformed


