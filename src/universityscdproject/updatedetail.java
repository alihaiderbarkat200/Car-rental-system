    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
     String inputID = orderid1.getText().trim();
    if (inputID.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter the Order ID to search.");
        return;
    }
    try {
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/car_rental_db", "root", "");
        String sql = "SELECT * FROM bookings WHERE `order-ID` = ?";
        PreparedStatement pst = con.prepareStatement(sql);
        pst.setString(1, inputID); 
        ResultSet rs = pst.executeQuery();

        if (rs.next()) {
            email1.setText(rs.getString("user_email"));
            Drivingno.setText(rs.getString("drivinglicence"));
            jDateChooser1.setDate(rs.getDate("pickup_date"));
            jDateChooser2.setDate(rs.getDate("return_date"));
            
            String currentStatus = rs.getString("status");
            JOptionPane.showMessageDialog(this, "Booking Found. Status: " + currentStatus);
        } else {
            JOptionPane.showMessageDialog(this, "Order ID: " + inputID + " not found!");
        }
        con.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }

    }//GEN-LAST:event_jButton2ActionPerformed

